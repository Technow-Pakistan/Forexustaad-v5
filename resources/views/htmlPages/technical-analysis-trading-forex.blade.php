                                <h4>
                                    What Is Technical Analysis
                                </h4>
                                
                                <p>
                                    Technical analysis is the science of using past trends as well as other data to give you an idea of what’s going to happen in the future. When it comes to this type of analysis, only technical data is considered. This means that charts become your best friend and that you don’t concern yourself much with fundamental information like product releases and management news.
                                </p>
                                
                                <p>
                                    Essentially, technical analysis is a process that’s based on the idea that in the market history often repeats itself. As a result, by studying what has happened throughout history, you can get a good idea of what’s going to happen in the future.
                                </p>
                                
                                <h4>
                                    The Four Most Important Technical Terms For Forex Traders To Understand
                                </h4>
                                
                                <p>
                                    While there are countless terms that you’ll learn as you delve deeper into technical analysis, there are four basic terms that are incredibly important to understand. Those terms include…
                                </p>
                                
                                <div>
                                    <ul>
                                    <li>
                                        <strong>Trend</strong> – First and foremost, the term trend is the most important term to know. This term explains the action of a currency pair moving consistently upward or consistently downward. If it is moving consistently upward, it’s trending upward, considered to be a bullish trend. If the currency pair is moving consistently downward, it is considered to be a downward or bearish trend.
                                    </li>
                                    <li>
                                        <strong>Support </strong> – Support is the point at which the value of a currency pair no longer falls and changes direction, moving upward. The support line can be found by drawing a line connecting the lowest points on the chart. When the pair nears this point, chances are that we will see a reversal and the value will head upward
                                    </li>
                                    <li>
                                        <strong>Resistance</strong> – Resistance is the exact opposite of support. It is the point at which the value of a currency is likely to stop heading up and changing directions. To find the resistance line, simply draw a line connecting the high points on the chart. When the value of a currency pair nears this line, it’s likely to reverse direction, heading downward.
                                    </li>
                                    <li>
                                        <strong>Breakout</strong> – A breakout is a term used to describe what happens when the value of a currency pair breaks below the support trend line or breaks above the resistance trend lines. Breakouts generally lead to massive movement in the direction of the break. So, if it breaks upward, chances are that we will see massive gains. However, if the currency pair breaks downward, chances are that we will see massive declines.
                                    </li>
                                </ul>
